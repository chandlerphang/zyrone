package com.zyrone.xml.merge;

public class XmlMergeException extends Exception {

    private static final long serialVersionUID = 1L;

    public XmlMergeException() {
        super();
    }

    public XmlMergeException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public XmlMergeException(String arg0) {
        super(arg0);
    }

    public XmlMergeException(Throwable arg0) {
        super(arg0);
    }

}
